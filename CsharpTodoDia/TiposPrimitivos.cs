public class TiposPrimitivos
{
    public void ExemploTipos()
    {
        // Exemplo de tipos primitivos em C#
        int inteiro = 10;
        double pontoFlutuante = 3.14;
        char caractere = 'A';
        bool booleano = true;
        // Imprime os valores dos tipos primitivos
        Console.WriteLine("\tExemplo de Tipos Primitivos:");
        Console.WriteLine("----------------------------");
        Console.WriteLine($"Inteiro: {inteiro}");
        Console.WriteLine($"Ponto Flutuante: {pontoFlutuante}");
        Console.WriteLine($"Caractere: {caractere}");
        Console.WriteLine($"Booleano: {booleano}");
        Console.WriteLine("----------------------------");
        Console.WriteLine();
    }
}
